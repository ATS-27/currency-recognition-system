"""Why MobileNetV2? We chose MobileNetV2, a pre-trained model, for two reasons:

Transfer Learning: It was previously trained on the massive ImageNet dataset, so it already knows how to recognize basic features like edges, textures, and shapes. We are leveraging this knowledge.

Efficiency: It's a lightweight model designed for speed, making it ideal for real-world applications (like a mobile app).

Time-Saving: It takes comparatively less time (2~3 hours for my dataset) compared to other heavy ML models both pre trained and newly made.
"""

